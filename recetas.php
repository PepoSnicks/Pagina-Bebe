        <h2>Puré de Manzana y Plátano</h2>
        <p><strong>Ingredientes:</strong></p>
        <ul>
            <li>1 manzana</li>
            <li>1 plátano maduro</li>
        </ul>
        <p><strong>Preparación:</strong></p>
        <ol>
            <li>Pela y corta la manzana en trozos.</li>
            <li>Pela el plátano y córtalo en rodajas.</li>
            <li>Cuece la manzana y el plátano al vapor hasta que estén suaves.</li>
            <li>Tritura la manzana y el plátano con un tenedor o procesador de alimentos hasta obtener una consistencia suave.</li>
            <li>Sirve tibio y disfruta.</li>
        </ol>

        <h2>Puré de Zanahoria y Calabaza</h2>
        <p><strong>Ingredientes:</strong></p>
        <ul>
            <li>1 zanahoria</li>
            <li>1 calabaza</li>
        </ul>
        <p><strong>Preparación:</strong></p>
        <ol>
            <li>Pela y corta la zanahoria y la calabaza en trozos.</li>
            <li>Cuece al vapor la zanahoria y la calabaza hasta que estén suaves.</li>
            <li>Tritura la zanahoria y la calabaza con un tenedor o procesador de alimentos hasta obtener una consistencia suave.</li>
            <li>Sirve tibio y disfruta.</li>
        </ol>

        <h2>Puré de Zanahoria y Papas</h2>
        <p><strong>Ingredientes:</strong></p>
        <ul>
            <li>Una papa grande</li>
            <li>Dos zanahorias</li>
            <li>Caldo de verdura sin sal</li>
            <li>Aceite de oliva</li>
        </ul>
        <p><strong>Preparación:</strong></p>
        <ol>
            <li>Pela las papas, las zanahorias y córtalas en trozos.</li>
            <li>En una olla echa un chorrito de aceite de oliva y calienta a fuego medio. Añade las papas y zanahorias y espera un par de minutos mientras remueves.</li>
            <li>Echa el caldo hasta cubrir las verduras completamente y un par de dedos más. Deja cocer unos 15 minutos o hasta que pinches la patata y la zanahoria y estén blandas.</li>
            <li>Cuando las verduras estén tiernas tritúralas en la batidora con una cucharada sopera del caldo de cocción hasta que quede una textura suave. Si el puré queda demasiado espeso añade un poco más de caldo.</li>
            <li>Pon una ración en un recipiente, añade un chorrito de aceite de oliva, remueve bien y sirve el puré tibio.</li>
        </ol>

        <h2>Puré de Verduras con Aguacate</h2>
        <p><strong>Ingredientes:</strong></p>
        <ul>
           <li>100g de brócoli (medio brócoli pequeño aprox.)</li> 
           <li>200g de calabaza</li>
           <li>Medio tomate</li>
           <li>La mitad de un aguacate pequeño</li>
        </ul>
        <p><strong>Preparación:</strong></p>
        <ol>
            <li>Lava y pela el calabaza y el tomate. Lava también el brócoli. Corta todo en dados pequeños.</li>
            <li>Echa las verduras en una olla y añade agua hasta justo cubrirlas.</li>
            <li>Pon la olla al fuego y cuando empiece a hervir cuece a fuego medio durante 15 minutos o hasta que las verduras estén tiernas.</li>
            <li>Escurre las verduras y ponlas en el vaso de la batidora. Tritura unos segundos hasta conseguir una textura suave. Puedes añadir algo de agua de la cocción si ves que te queda demasiado espeso.</li>
            <li>Corta el aguacate por la mitad y quítale el hueso. Saca toda la pulpa del aguacate con una cuchara. La mayor parte de los nutrientes se concentran en la parte oscura de la pulpa, junto a la piel, así que raspala bien.</li>
            <li>Machaca bien el aguacate con un tenedor y añádelo al puré cuando ya esté tibio. Remueve bien para que se mezcle con las verduras. Sírvelo en un envase y ya puede tomarlo tu bebé.</li>
        </ol>

