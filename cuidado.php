        <h1>Consejos sobre Cuidado Infantil</h1>

        <p>¡Aquí encontrarás algunos consejos útiles para cuidar a tu bebé!</p>

        <h2>Consejo 1: Alimentación</h2>
        <p>Asegúrate de que tu bebé esté recibiendo la cantidad adecuada de leche materna o fórmula según su edad. Consulta con un pediatra si tienes preguntas sobre la alimentación de tu bebé.</p>

        <h2>Consejo 2: Sueño</h2>
        <p>Establece una rutina de sueño regular para tu bebé y crea un ambiente tranquilo y relajado para ayudarlo a dormir mejor.</p>

        <h2>Consejo 3: Higiene</h2>
        <p>Mantén a tu bebé limpio y seco cambiando regularmente sus pañales y bañándolo con agua tibia. No te olvides de limpiar su ombligo con alcohol después del baño.</p>

        <h2>Consejo 4: Seguridad</h2>
        <p>Asegura tu hogar eliminando cualquier objeto pequeño o peligroso al alcance de tu bebé y utilizando correctamente los dispositivos de seguridad, como las sillas de auto y los protectores de enchufes.</p>

        <h2>Consejo 5: Estimulación</h2>
        <p>Interactúa con tu bebé a través del juego y la conversación para estimular su desarrollo cognitivo y emocional. Además, asegúrate de proporcionarle suficiente tiempo boca abajo para fortalecer sus músculos.</p>

        <h2>Consejo 6: Habla con tu bebé</h2>
        <p>Aunque no lo creas, los bebés también hablan y les gusta platicar con las personas que les rodean. Cuando los bebés empiezan a balbucear, es porque quieren y están platicando contigo, por lo que es el momento ideal para que te diviertas hablando con él.</p>
        <p>Estas son algunas de las cosas que puedes hacer mientras platicas con tu bebé:</p>
        <li>Haz ruidos, aunque parezcan tontos.</li>
        <li>Esconde tu rostro detrás de las manos y vuelve a aparecer para que el bebé se ría.</li>
        <li>Cántale canciones.</li>
        <li>Muéstrale y léele libros ilustrados.</li>

        <h2>Consejo 7: No olvides su seguridad</h2>
        <p>Cuando los bebés empiezan a tener cierta independencia, es importante estar al pendiente de sus movimientos, ya que ellos no saben lo que podría ponerlos en peligro.</p>

        <p>Si mantienes a tu bebé en un lugar seguro, como una cuna o un corral, evitará caídas, quemaduras, envenenamiento, asfixia y otras lesiones. Preparar tu casa a prueba de niños también puede ser útil para mantener a su bebé seguro.</p>

        <h2>Consejo 8: No te asutes</h2>
        <p>La mayoría de los bebés pasan por algunas dificultades que pueden ser muy frustrantes para los padres, pero pronto las superarán:</p>
        <li>Cólicos. No son causados por algo que hayas hecho. Generalmente desaparecen en unos 4 a 5 meses.</li>
        <li>Problemas para dormir. Algunos bebés tendrán problemas para dormir o para despertarse con frecuencia durante la noche.</li>
        <li>Apego excesivo a los padres. Cuando los bebés no ven a ciertas personas (incluso amigos cercanos y abuelos) con mucha frecuencia, pueden tenerles miedo.</li>
